def dfermi(dk,eta,theta):

#! this routine computes the fermi-dirac integrals f(dk,eta,theta) of
#! index dk, with degeneracy parameter eta and relativity parameter theta.

# input
#dk: the double precision index of the fermi-dirac function,
#eta: the degeneracy parameter
#theta: the relativity parameter.

#output 
#fd: is computed by applying three 10-point gauss-legendre
#and one 10-point gauss-laguerre rules over four appropriate subintervals.
# fdeta: the derivative of f with respect to eta,
# fdthetha: the derivative of f with theta.

# reference: j.m. aparicio, apjs 117, 632 1998

  d=3.3609e0 
  sg  = 9.1186e-2 
  a1  = 6.7774e0 
  b1  = 1.1418e0 
  c1  = 2.9826e0 
  a2  = 3.7601e0 
  b2  = 9.3719e-2 
  c2  = 2.1063e-2 
  d2  = 3.1084e1 
  e2  = 1.0056e0 
  a3  = 7.5669e0 
  b3  = 1.1695e0 
  c3  = 7.5416e-1 
  d3  = 6.6558e0 
  e3  = -1.2819e-1 

  par=[dk,eta,theta]

#definition of xi:
  eta1=sg*(eta-d)
  if eta1<=5.e1:
     xi=np.log(1.e0+np.exp(eta1))/sg
  else:
     xi=eta-d

  xi2 = xi*xi

#   definition of the x_i:
  x1 = (a1  +b1*xi+c1*   xi2) / (1.e0+c1*xi)
  x2 = (a2  +b2*xi+c2*d2*xi2) / (1.e0+e2*xi+c2*   xi2)
