# routine dfermi gets the fermi-dirac functions and their derivaties
# routine fdfunc1 forms the integrand of the fermi-dirac functions
# routine fdfunc2 same as fdfunc but with the change of variable z**2=x
# routine dqleg010 does 10 point gauss-legendre integration  9 fig accuracy
# routine dqleg020 does 20 point gauss-legendre integration 14 fig accuracy
# routine dqleg040 does 40 point gauss-legendre integration 18 fig accuracy
# routine dqleg080 does 80 point gauss-legendre integration 32 fig accuracy
# routine dqlag010 does 10 point gauss-laguerre integration  9 fig accuracy
# routine dqlag020 does 20 point gauss-laguerre integration 14 fig accuracy
# routine dqlag040 does 40 point gauss-laguerre integration 18 fig accuracy
# routine dqlag080 does 80 point gauss-laguerre integration 32 fig accuracy

import numpy as np

def fdfunc1(x,par):
# forms the fermi-dirac integrand and its first and second
# derivatives with eta and theta.
# input:
# x is the integration variable
# par[0] is the double precision index
# par[1] is the degeneravy parameter
# par[2] is the relativity parameter 

# output:
#fd is the integrand
# fdeta is the first derivative with eta
# fdeta2 is the second derivative with eta
# fdtheta is the first derivative with theta
# fdtheta2 is the second derivative with theta
# fdetadtheta is the mixed second derivative 
# initialize
   dk    = par[0]
   eta   = par[1]
   theta = par[2]
   xdk   = np.power(x,dk)
   xst   = 1.0e0 + 0.5e0*x*theta
   dxst  = np.sqrt(xst)

#   avoid overflow in the exponentials at large x
   if ((x-eta) < 1.0e2):
       factor      = np.exp(x-eta)
       denom       = factor + 1.0e0
       denomi      = 1.0e0/denom
       fd          = xdk * dxst * denomi
       fdeta       = fd * factor * denomi
       fdeta2      = (2.0e0 * factor * denomi - 1.0e0)*fdeta
       denom2      = 1.0e0/(4.0e0 * xst)
       fdtheta     = fd * x * denom2
       fdtheta2    = -fdtheta * x * denom2
       fdetadtheta = fdtheta * factor * denomi
   else:
       factor      = np.exp(eta-x)
       fd          = xdk * dxst * factor
       fdeta       = fd
       fdeta2      = fd
       denom2      = 1.0e0/(4.0e0 * xst)
       fdtheta     = fd * x * denom2
       fdtheta2    = -fdtheta * x * denom2
       fdetadtheta = fdtheta
   return [fd,fdeta,fdtheta, fdeta2,fdtheta2,fdetadtheta]
 

def fdfunc2(x,par):  
# forms the fermi-dirac integrand and its first and second
# derivatives with eta and theta when the variable change z**2=x
# has been made.

# input:
# x is the integration variable
# par[0] is the double precision index
# par[1] is the degeneravy parameter
# par[2] is the relativity parameter

# output:
# fd is the integrand
# fdeta is the first derivative with eta
# fdeta2 is the second derivative with eta
# fdtheta is the first derivative with theta
# fdtheta2 is the second derivative with theta
# fdetadtheta is the mixed second derivative

   dk    = par[0]
   eta   = par[1]
   theta = par[2]
   xsq   = x * x
   xdk   = np.power(x,2.0e0 * dk + 1.0e0)
   xst   = 1.0e0 + 0.5e0 * xsq * theta
   dxst  = np.sqrt(xst)
#  avoid an overflow in the denominator at large x:
   if ((xsq-eta) < 1.e2): 
       factor      = np.exp(xsq - eta)
       denom       = factor + 1.0e0
       denomi      = 1.0e0/denom
       fd          = 2.0e0 * xdk * dxst * denomi
       fdeta       = fd * factor * denomi
       fdeta2      = (2.0e0 * factor * denomi - 1.0e0)*fdeta
       denom2      = 1.0e0/(4.0e0 * xst)
       fdtheta     = fd * xsq * denom2
       fdtheta2    = -fdtheta * xsq * denom2
       fdetadtheta = fdtheta * factor * denomi
   else:
       factor      = np.exp(eta - xsq)
       fd          = 2.0e0 * xdk * dxst * factor
       fdeta       = fd
       fdeta2      = fd
       denom2      = 1.0e0/(4.0e0 * xst)
       fdtheta     = fd * xsq * denom2
       fdtheta2    = -fdtheta * xsq * denom2
       fdetadtheta = fdtheta
   return [fd,fdeta,fdtheta, fdeta2,fdtheta2,fdetadtheta]
   


def dqleg010(f,a,b,par,n): 
# 10 point gauss-legendre rule for the fermi-dirac function and
# its derivatives with respect to eta and theta.
# input 
#f: the name of the subroutine containing the integrand,
#a: lower end point of the interval 
#b: higher end point
#par: an array of constant parameters to be passed to f
#n:  the length of the par array. 

# output result is the 
# approximation from applying the 10-point gauss-legendre rule,
#
#drdeta: the derivative with respect to eta
#drdtheta: the derivative with respect to theta.

#the abscissae and weights are given for the interval (-1,1).
# xg     - abscissae of the 20-point gauss-legendre rule
#          for half of the usual run (-1,1), i.e.
#          the positive nodes of the 20-point rule
# wg     - weights of the 20-point gauss rule.
# abscissae and weights were evaluated with 100 decimal digit arithmetic.
    xg=np.zeros(5)
    xg[0] = 1.48874338981631210884826001129719984e-1 
    xg[1] = 4.33395394129247190799265943165784162e-1 
    xg[2] = 6.79409568299024406234327365114873575e-1 
    xg[3] = 8.65063366688984510732096688423493048e-1 
    xg[4] = 9.73906528517171720077964012084452053e-1 

    wg=np.zeros(5)
    wg[0] = 2.95524224714752870173892994651338329e-1 
    wg[1] =   2.69266719309996355091226921569469352e-1 
    wg[2] =  2.19086362515982043995534934228163192e-1 
    wg[3] = 1.49451349150580593145776339657697332e-1 
    wg[4] =  6.66713443086881375935688098933317928e-2 
  
    center       = 0.5e0 * (a+b)
    hlfrun       = 0.5e0 * (b-a)
    result       = 0.0e0
    drdeta       = 0.0e0
    drdtheta     = 0.0e0
    drdeta2      = 0.0e0
    drdtheta2    = 0.0e0
    drdetadtheta = 0.0e0
    for j in range(5):
        absc1 = center + hlfrun*xg[j]
        absc2 = center - hlfrun*xg[j]
        fval1 = f(absc1, par)
        fval2 = f(absc2, par)
        result       = result    + (fval1[0] + fval2[0])*wg[j]
        drdeta       = drdeta    + (fval1[1] + fval2[1])*wg[j]
        drdtheta     = drdtheta  + (fval1[2] + fval2[2])*wg[j]
        drdeta2      = drdeta2   + (fval1[3] + fval2[3])*wg[j]
        drdtheta2    = drdtheta2 + (fval1[4] + fval2[4])*wg[j]
        drdetadtheta = drdetadtheta + (fval1[5] + fval2[5])*wg[j]
    result       *=  hlfrun
    drdeta       *=  hlfrun
    drdtheta     *=  hlfrun
    drdeta2      *=  hlfrun
    drdtheta2    *=  hlfrun
    drdetadtheta *=  hlfrun
    return [result, drdeta, drdtheta, drdeta2, drdtheta2, drdetadtheta]
      
      
